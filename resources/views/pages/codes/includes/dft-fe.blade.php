    <!-- Introduction -->
    <div class="mb-4">
        <p class="lead text-dark">
            Electronic structure calculations have played a significant role in the investigation of materials
            properties over the past few decades. The Kohn-Sham approach to density functional theory (DFT)
            has made quantum-mechanically informed calculations on ground-state materials properties
            computationally tractable, providing crucial insights into a wide range of materials behaviors.
        </p>
